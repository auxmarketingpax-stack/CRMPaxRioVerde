begin;

create table if not exists public.stage_type_catalog (
  id bigint generated by default as identity primary key,
  name text not null unique,
  created_at timestamptz not null default now(),
  created_by uuid null
);

alter table public.stage_type_catalog enable row level security;

drop policy if exists stage_type_catalog_select_authenticated on public.stage_type_catalog;
create policy stage_type_catalog_select_authenticated
on public.stage_type_catalog
for select
to authenticated
using (auth.uid() is not null);

drop policy if exists stage_type_catalog_insert_authenticated on public.stage_type_catalog;
create policy stage_type_catalog_insert_authenticated
on public.stage_type_catalog
for insert
to authenticated
with check (auth.uid() is not null);

drop policy if exists stage_type_catalog_update_authenticated on public.stage_type_catalog;
create policy stage_type_catalog_update_authenticated
on public.stage_type_catalog
for update
to authenticated
using (auth.uid() is not null)
with check (auth.uid() is not null);

drop policy if exists stage_type_catalog_delete_authenticated on public.stage_type_catalog;
create policy stage_type_catalog_delete_authenticated
on public.stage_type_catalog
for delete
to authenticated
using (auth.uid() is not null);

insert into public.stage_type_catalog (name)
select distinct btrim(custom_stage_type)
from public.stages
where custom_stage_type is not null
  and btrim(custom_stage_type) <> ''
on conflict (name) do nothing;

do $$
declare
  fn record;
  target_search_path text;
begin
  for fn in
    select
      n.nspname as schema_name,
      p.proname as function_name,
      pg_get_function_identity_arguments(p.oid) as args
    from pg_proc p
    join pg_namespace n on n.oid = p.pronamespace
    where n.nspname = 'public'
      and p.proname in ('is_master_admin', 'set_updated_at')
  loop
    target_search_path := case
      when fn.function_name = 'is_master_admin' then 'public, auth'
      else 'public'
    end;

    execute format(
      'alter function %I.%I(%s) set search_path = %s',
      fn.schema_name,
      fn.function_name,
      fn.args,
      target_search_path
    );
  end loop;
end
$$;

do $$
declare
  ownership_column text;
begin
  if exists (
    select 1
    from information_schema.tables
    where table_schema = 'public'
      and table_name = 'audit_logs'
  ) then
    execute 'drop policy if exists audit_logs_insert_authenticated on public.audit_logs';

    select column_name
    into ownership_column
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'audit_logs'
      and column_name in ('user_id', 'created_by', 'actor_id')
    order by case column_name
      when 'user_id' then 1
      when 'created_by' then 2
      when 'actor_id' then 3
      else 99
    end
    limit 1;

    if ownership_column is not null then
      execute format(
        'create policy audit_logs_insert_authenticated on public.audit_logs for insert to authenticated with check (auth.uid() = %I)',
        ownership_column
      );
    end if;
  end if;
end
$$;

commit;

